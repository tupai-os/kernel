.extern _vga_cursor.boot

.global _vga_boot_cursor

.code32
.section .text.boot
	_vga_boot_cursor:
		push %ebp
		mov %esp, %ebp

		movl (_vga_cursor.boot), %eax

		pop %ebp
		ret
